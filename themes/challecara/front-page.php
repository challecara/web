      <h2>九州アプリチャレンジ・キャラバンについて</h2>
      <div>
        <p>九州アプリチャレンジ・キャラバン（以下、チャレキャラ）は、一歩先ゆく実践的なプログラミングスキルや問題解決能力を身につけたいと感じている学生のためのアプリ開発イベントです。7月から順次開催されるイベントを通じてアプリの企画・開発を進め、最終ゴールである12月のコンテストを目指します。</p>
        <p>期間中は現役エンジニアから構成されるメンターが、オンライン・オフラインでそれぞれの挑戦をバックアップ。学校のようなカリキュラムはありませんが、先に進みたいという情熱さえあれば、いくらでも学ぶことできる最高の環境を提供します。</p>
        <p>エントリー時点で九州にある高等教育機関（大学・専門学校・高等専門学校など）のキャンパスに通う学生であれば、特に参加制限はありません。最優秀賞の賞金30万円を目指して突っ走るもよし、まずはオリジナルのアプリを作り上げることを目指すもよし、それぞれの想いをチャレキャラはサポートします。</p>
        <p>目の前に現れる壁を1つずつクリアした先には、あなたの想像を越えた成長が待っています。</p>
